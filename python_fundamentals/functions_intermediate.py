import random
print(random.random()) # returns a random floating number between 0.000 to 1.000
print(random.random()*50) # returns a float between 0.000 to 50.000
print(random.random()*100) # returns a float between 0.000 to 100.000
print(random.randrange(50, 100)) # returns an integer between 50 to 100
print(random.randrange(50, 500)) # returns an integer between 50 to 500
